-- ============================================================
-- Виконати в Supabase SQL Editor
-- Це дозволить всім користувачам бачити нормативні документи

-- Додати політику: всі користувачі можуть читати нормативні документи
DROP POLICY IF EXISTS "Users can view regulatory documents" ON documents;
CREATE POLICY "Users can view regulatory documents"
ON documents FOR SELECT
USING (
  document_type = 'regulatory'  -- Всі нормативні документи доступні всім
);

-- Перевірка: політика має працювати разом з існуючою
-- Тепер користувач може бачити:
-- 1. Свої власні документи (user_id = auth.uid())
-- 2. ВСІ нормативні документи (document_type = 'regulatory')

-- ============================================================
-- Перевірка
-- ============================================================

-- Перевірити всі політики для documents
SELECT
  schemaname,
  tablename,
  policyname,
  cmd,
  qual
FROM pg_policies
WHERE schemaname = 'public'
  AND tablename = 'documents'
ORDER BY policyname;

