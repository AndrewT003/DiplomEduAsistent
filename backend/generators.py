def chat_with_document(context: str, question: str, history: list, regulatory_docs: list = None) -> str:
    # Формуємо інформацію про нормативні документи для системного промпту
    regulatory_system_info = ""
    if regulatory_docs and len(regulatory_docs) > 0:
        regulatory_system_info = "\n\n" + "="*50 + "\n"
        regulatory_system_info += "ЗАВАНТАЖЕНІ НОРМАТИВНІ ДОКУМЕНТИ В СИСТЕМІ:\n"
        regulatory_system_info += "="*50 + "\n"
        for idx, doc in enumerate(regulatory_docs, 1):
            category = doc.get('regulatory_category', 'не вказана')
            regulatory_system_info += f"{idx}. {doc['filename']} (категорія: {category})\n"
        regulatory_system_info += "="*50 + "\n"
    else:
        regulatory_system_info = "\n\nНормативні документи: поки не завантажено."

    print(f"📋 Передаю асистенту список нормативних документів: {len(regulatory_docs) if regulatory_docs else 0}")

    messages = [{"role": "system", "content": SYSTEM_PROMPT + regulatory_system_info}]

    for msg in history:
        messages.append(msg)

    # Формуємо контекст для user message з ОБОВ'ЯЗКОВИМ списком нормативів
    user_context = f"Контекст з документу:\n{context}\n"

    # ВАЖЛИВО: Додаємо список нормативних документів БЕЗПОСЕРЕДНЬО в user message
    # щоб LLM не міг його ігнорувати
    if regulatory_docs and len(regulatory_docs) > 0:
        user_context += "\n" + "🔴"*25 + "\n"
        user_context += "⚠️ СПИСОК НОРМАТИВНИХ ДОКУМЕНТІВ (використовуй ТІЛЬКИ цей список!):\n"
        user_context += "="*50 + "\n"
        for idx, doc in enumerate(regulatory_docs, 1):
            category = doc.get('regulatory_category', 'не вказана')
            user_context += f"{idx}. {doc['filename']} (категорія: {category})\n"
        user_context += "="*50 + "\n"
        user_context += "⚠️ ВАЖЛИВО: Це ПОВНИЙ і ЄДИНИЙ список! НЕ вигадуй інші документи!\n"
        user_context += "🔴"*25 + "\n\n"

    user_context += f"Питання: {question}"

    messages.append({
        "role": "user",
        "content": user_context
    })

    response = groq_client.chat.completions.create(
        model="llama-3.1-8b-instant",
        messages=messages,
        max_tokens=2000,  # Збільшено для детальніших відповідей
        temperature=0.3   # Трохи більше креативності для кращих пояснень
    )

    return response.choices[0].message.content