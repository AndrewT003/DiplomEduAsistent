from config import groq_client

def extract_topic_from_context(context: str) -> str:
    """Витягує тему/назву заняття з контексту документа"""
    prompt = f"""Проаналізуй наступний текст і витягни головну тему або назву заняття.
Поверни ТІЛЬКИ назву теми одним реченням (максимум 10 слів), без додаткових пояснень та БЕЗ markdown символів (#, *, _).
Поверни чистий текст без форматування.
Якщо тема не очевидна, поверни "Навчальний матеріал".

Текст:
{context[:2000]}"""

    try:
        response = groq_client.chat.completions.create(
            model="llama-3.1-8b-instant",
            messages=[{"role": "user", "content": prompt}],
            max_tokens=50,
            temperature=0.3
        )
        topic = response.choices[0].message.content.strip()
        # Прибираємо лапки якщо є
        topic = topic.strip('"').strip("'")
        # Прибираємо markdown символи (#, *, _, тощо)
        import re
        topic = re.sub(r'^[#\*_\-\s]+', '', topic)  # Прибираємо на початку
        topic = re.sub(r'[#\*_]+', '', topic)  # Прибираємо всередині
        topic = topic.strip()
        return topic if topic else "Навчальний матеріал"
    except:
        return "Навчальний матеріал"


